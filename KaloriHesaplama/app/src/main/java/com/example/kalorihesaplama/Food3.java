package com.example.kalorihesaplama;

public class Food3 {

    private String miktar3;
    private String ad3;
    private String kalori3;

    public Food3() {

    }

    public Food3(String miktar3, String ad3, String kalori3) {
        this.miktar3 = miktar3;
        this.ad3 = ad3;
        this.kalori3 = kalori3;
    }

    public String getMiktar3() {
        return miktar3;
    }

    public void setMiktar3(String miktar3) {
        this.miktar3 = miktar3;
    }

    public String getAd3() {
        return ad3;
    }

    public void setAd3(String ad3) {
        this.ad3 = ad3;
    }

    public String getKalori3() {
        return kalori3;
    }

    public void setKalori3(String kalori3) {
        this.kalori3 = kalori3;
    }
}
